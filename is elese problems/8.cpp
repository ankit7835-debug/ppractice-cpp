 #include <iostream>
using namespace std;

int main() {
    int marks;
     cin>>marks;
     
     if(marks>90){
         cout<<"A"<<endl;
         
     }
	else if(marks>70){
	    cout<<"B"<<endl;
	    
	    
	}
	
	else if (marks>=40){
	    cout<<"c"<<endl;
	    
	}
	else{
	    cout<<"f"<<endl;
	    
	}
	return 0;
	
}
